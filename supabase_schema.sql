-- Attach trigger to tables with updated_at
DO $$
BEGIN
  IF NOT EXISTS (SELECT 1 FROM pg_trigger WHERE tgname = 'trigger_update_timestamp_products') THEN
    CREATE TRIGGER trigger_update_timestamp_products
    BEFORE UPDATE ON public.products
    FOR EACH ROW EXECUTE FUNCTION public.update_timestamp();
  END IF;
  IF NOT EXISTS (SELECT 1 FROM pg_trigger WHERE tgname = 'trigger_update_timestamp_packages') THEN
    CREATE TRIGGER trigger_update_timestamp_packages
    BEFORE UPDATE ON public.packages
    FOR EACH ROW EXECUTE FUNCTION public.update_timestamp();
  END IF;
  IF NOT EXISTS (SELECT 1 FROM pg_trigger WHERE tgname = 'trigger_update_timestamp_staffs') THEN
    CREATE TRIGGER trigger_update_timestamp_staffs
    BEFORE UPDATE ON public.staffs
    FOR EACH ROW EXECUTE FUNCTION public.update_timestamp();
  END IF;
  IF NOT EXISTS (SELECT 1 FROM pg_trigger WHERE tgname = 'trigger_update_timestamp_customers') THEN
    CREATE TRIGGER trigger_update_timestamp_customers
    BEFORE UPDATE ON public.customers
    FOR EACH ROW EXECUTE FUNCTION public.update_timestamp();
  END IF;
  IF NOT EXISTS (SELECT 1 FROM pg_trigger WHERE tgname = 'trigger_update_timestamp_appointments') THEN
    CREATE TRIGGER trigger_update_timestamp_appointments
    BEFORE UPDATE ON public.appointments
    FOR EACH ROW EXECUTE FUNCTION public.update_timestamp();
  END IF;
  IF NOT EXISTS (SELECT 1 FROM pg_trigger WHERE tgname = 'trigger_update_timestamp_online_orders') THEN
    CREATE TRIGGER trigger_update_timestamp_online_orders
    BEFORE UPDATE ON public.online_orders
    FOR EACH ROW EXECUTE FUNCTION public.update_timestamp();
  END IF;
  IF NOT EXISTS (SELECT 1 FROM pg_trigger WHERE tgname = 'trigger_update_timestamp_settings') THEN
    CREATE TRIGGER trigger_update_timestamp_settings
    BEFORE UPDATE ON public.settings
    FOR EACH ROW EXECUTE FUNCTION public.update_timestamp();
  END IF;
  IF NOT EXISTS (SELECT 1 FROM pg_trigger WHERE tgname = 'trigger_update_timestamp_employees') THEN
    CREATE TRIGGER trigger_update_timestamp_employees
    BEFORE UPDATE ON public.employees
    FOR EACH ROW EXECUTE FUNCTION public.update_timestamp();
  END IF;
  IF NOT EXISTS (SELECT 1 FROM pg_trigger WHERE tgname = 'trigger_update_timestamp_attendance') THEN
    CREATE TRIGGER trigger_update_timestamp_attendance
    BEFORE UPDATE ON public.attendance
    FOR EACH ROW EXECUTE FUNCTION public.update_timestamp();
  END IF;
END;
$$ LANGUAGE plpgsql;

