 -- Supabase / PostgreSQL schema for SPA app
-- Use in Supabase SQL editor or psql

DROP TABLE IF EXISTS public.settings CASCADE;
CREATE TABLE public.settings (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  spaName text NOT NULL,
  address text,
  phone text,
  footerNote text,
  defaultBaseSalary integer DEFAULT 6000000,
  defaultServiceComm integer DEFAULT 10,
  defaultConsultComm integer DEFAULT 5,
  packagePointRate integer DEFAULT 1,
  productPointRate integer DEFAULT 1,
  referralPoints integer DEFAULT 500,
  referralStocks integer DEFAULT 200,
  currencySymbol text DEFAULT 'đ',
  taxRate integer DEFAULT 10,
  reminderLeadTime integer DEFAULT 60,
  businessHours text,
  signatureNote text,
  emailSupport text,
  invoicePrefix text DEFAULT 'HD',
  totalStocks bigint DEFAULT 10000000,
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now()
);

DROP TABLE IF EXISTS public.products CASCADE;
CREATE TABLE public.products (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name text NOT NULL,
  price numeric(12, 0) NOT NULL,
  stock integer DEFAULT 0,
  rewardPoints integer DEFAULT 0,
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now()
);
CREATE INDEX IF NOT EXISTS idx_products_name ON public.products (name);

DROP TABLE IF EXISTS public.packages CASCADE;
CREATE TABLE public.packages (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name text NOT NULL,
  sessions integer NOT NULL DEFAULT 1,
  price numeric(12, 0) NOT NULL,
  rewardPoints integer DEFAULT 0,
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now()
);
CREATE INDEX IF NOT EXISTS idx_packages_name ON public.packages (name);

DROP TABLE IF EXISTS public.staffs CASCADE;
CREATE TABLE public.staffs (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name text NOT NULL,
  phone text,
  baseSalary integer DEFAULT 0,
  serviceComm integer DEFAULT 0,
  consultComm integer DEFAULT 0,
  stocks numeric DEFAULT 0,
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now()
);
CREATE INDEX IF NOT EXISTS idx_staffs_phone ON public.staffs (phone);

DROP TABLE IF EXISTS public.customers CASCADE;
CREATE TABLE public.customers (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name text NOT NULL,
  phone text UNIQUE,
  referral_code text,
  referred_by text,
  referral_rewarded boolean DEFAULT false,
  my_packages jsonb DEFAULT '[]',
  points integer DEFAULT 0,
  stocks numeric DEFAULT 0,
  history jsonb DEFAULT '[]',
  shares numeric DEFAULT 0,
  balance numeric DEFAULT 0,
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now()
);
CREATE INDEX IF NOT EXISTS idx_customers_phone ON public.customers (phone);

DROP TABLE IF EXISTS public.appointments CASCADE;
CREATE TABLE public.appointments (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  customer_name text NOT NULL,
  customer_phone text,
  service text NOT NULL,
  ktv text,
  date date,
  time text,
  price numeric(12, 0) DEFAULT 0,
  status text DEFAULT 'Chờ phục vụ',
  note text,
  is_reminded boolean DEFAULT false,
  is_approved boolean DEFAULT false,
  shared_update boolean DEFAULT false,
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now()
);
CREATE INDEX IF NOT EXISTS idx_appointments_customer_phone ON public.appointments (customer_phone);
CREATE INDEX IF NOT EXISTS idx_appointments_status ON public.appointments (status);

DROP TABLE IF EXISTS public.online_orders CASCADE;
CREATE TABLE public.online_orders (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  customer_name text,
  customer_phone text,
  order_type text,
  item_id bigint,
  item_name text,
  quantity integer DEFAULT 1,
  total_price numeric(12, 0) DEFAULT 0,
  reward_points integer DEFAULT 0,
  payment_method text,
  payment_proof text,
  status text DEFAULT 'pending',
  order_date date,
  notes text,
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now()
);
CREATE INDEX IF NOT EXISTS idx_online_orders_status ON public.online_orders (status);
CREATE INDEX IF NOT EXISTS idx_online_orders_customer_phone ON public.online_orders (customer_phone);

DROP TABLE IF EXISTS public.customer_treatment_logs CASCADE;
CREATE TABLE public.customer_treatment_logs (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  customer_id bigint NOT NULL,
  date date,
  service text,
  staff text,
  note text,
  images jsonb DEFAULT '{}',
  skinCondition text,
  productsUsed text,
  nextStep text,
  sessionProgress text,
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now(),
  FOREIGN KEY (customer_id) REFERENCES public.customers (id) ON DELETE CASCADE
);
CREATE INDEX IF NOT EXISTS idx_treatment_logs_customer_id ON public.customer_treatment_logs (customer_id);

DROP TABLE IF EXISTS public.chat_messages CASCADE;
CREATE TABLE public.chat_messages (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  chat_type text,
  sender text,
  target_phone text,
  text text,
  created_at timestamptz NOT NULL DEFAULT now()
);
CREATE INDEX IF NOT EXISTS idx_chat_messages_target_phone ON public.chat_messages (target_phone);

DROP TABLE IF EXISTS public.share_transactions CASCADE;
CREATE TABLE public.share_transactions (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  customer_id bigint NOT NULL,
  type text,
  amount numeric DEFAULT 0,
  created_at timestamptz NOT NULL DEFAULT now(),
  FOREIGN KEY (customer_id) REFERENCES public.customers (id) ON DELETE CASCADE
);
CREATE INDEX IF NOT EXISTS idx_share_transactions_customer_id ON public.share_transactions (customer_id);

DROP TABLE IF EXISTS public.employees CASCADE;
CREATE TABLE public.employees (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name text,
  phone text,
  role text,
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now()
);

DROP TABLE IF EXISTS public.attendance CASCADE;
CREATE TABLE public.attendance (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  employee_id bigint NOT NULL,
  check_in timestamptz,
  check_out timestamptz,
  notes text,
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now(),
  FOREIGN KEY (employee_id) REFERENCES public.employees (id) ON DELETE CASCADE
);

-- Update timestamp trigger helper
CREATE OR REPLACE FUNCTION public.update_timestamp()
RETURNS trigger AS $$
BEGIN
  NEW.updated_at = now();
  RETURN NEW;
END;
$$ LANGUAGE plpgsql;

